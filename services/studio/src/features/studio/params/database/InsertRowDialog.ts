function getInputKind(columnType: string): 'text' | 'number' | 'boolean' | 'date' | 'datetime' | 'json' {
  const lower = columnType.toLowerCase();
  if (['integer', 'int', 'int4', 'int8', 'bigint', 'smallint', 'decimal', 'numeric',
       'real', 'double precision', 'float', 'float4', 'float8', 'money'].some(t => lower === t || lower.startsWith(t))) {
    return 'number';
  }
  if (lower === 'boolean' || lower === 'bool') return 'boolean';
  if (lower === 'date') return 'date';
  if (['timestamp', 'timestamptz', 'datetime'].some(t => lower.startsWith(t)) || lower.startsWith('time')) return 'datetime';
  if (lower === 'json' || lower === 'jsonb') return 'json';
  return 'text';
}

function isAutoGenerated(col: ColumnDTO): boolean {
  if (!col.primaryKey) return false;
  const lower = col.type.toLowerCase();
  if (['serial', 'bigserial', 'smallserial'].includes(lower)) return true;
  const def = (col.defaultValue || '').toLowerCase();
  if (def.includes('nextval') || def.includes('auto_increment') || def.includes('generated')) return true;
  return false;
}

function isRequired(col: ColumnDTO): boolean {
  if (isAutoGenerated(col)) return false;
  return !col.nullable && !col.defaultValue;
}

