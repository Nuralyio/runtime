  @property({ type: String }) tableName = '';
  @property({ type: String }) schemaName = '';
  @property({ type: Array }) columns: ColumnDTO[] = [];

  @state() private fieldValues: Record<string, any> = {};
  @state() private inserting = false;
  @state() private result: { success: boolean; error?: string; affectedRows?: number } | null = null;

  private get editableColumns(): ColumnDTO[] {
    return this.columns.filter(col => !isAutoGenerated(col));
  }

  private handleFieldChange(columnName: string, value: any) {
    this.fieldValues = { ...this.fieldValues, [columnName]: value };
  }

  private handleInsert() {
    const fields: Record<string, any> = {};
    for (const col of this.editableColumns) {
      const value = this.fieldValues[col.name];
      if (value !== undefined && value !== '' && value !== null) {
        fields[col.name] = value;
      }
    }

    this.inserting = true;
    this.result = null;
    this.dispatchEvent(new CustomEvent('insert-row', {
      bubbles: true,
      composed: true,
      detail: { fields },
    }));
  }

  private handleClose() {
    this.result = null;
    this.dispatchEvent(new CustomEvent('close-insert-panel', { bubbles: true, composed: true }));
  }

  public setResult(result: { success: boolean; error?: string; affectedRows?: number }) {
    this.inserting = false;
    this.result = result;
    if (result.success) {
      this.fieldValues = {};
    }
  }

  public resetState() {
    this.inserting = false;
    this.result = null;
    this.fieldValues = {};
  }

  private renderField(col: ColumnDTO) {
    const kind = getInputKind(col.type);
    const required = isRequired(col);
    const value = this.fieldValues[col.name];
    const hint = col.defaultValue ? `Default: ${col.defaultValue}` : col.nullable ? 'Optional' : '';

    return html`
      <div class="config-field">
        <label>
          ${col.name}
          <span class="col-type">${col.type}</span>
          ${required ? html`<span class="required-mark">*</span>` : nothing}
        </label>

        ${kind === 'boolean' ? html`
          <div class="checkbox-field">
            <nr-checkbox
              ?checked=${value === true || value === 'true'}
              @nr-change=${(e: CustomEvent) => this.handleFieldChange(col.name, e.detail?.checked ?? (e.target as any)?.checked)}
            ></nr-checkbox>
          </div>
        ` : kind === 'date' || kind === 'datetime' ? html`
          <nr-datepicker
            .value=${value || ''}
            placeholder=${kind === 'datetime' ? 'YYYY-MM-DD HH:mm:ss' : 'YYYY-MM-DD'}
            @nr-change=${(e: CustomEvent) => this.handleFieldChange(col.name, e.detail?.value ?? (e.target as any)?.value)}
          ></nr-datepicker>
        ` : kind === 'number' ? html`
          <nr-input
            type="number"
            .value=${value != null ? String(value) : ''}
            placeholder=${`Enter ${col.name}`}
            @nr-input=${(e: CustomEvent) => {
              const raw = e.detail?.value ?? (e.target as any)?.value;
              this.handleFieldChange(col.name, raw !== '' ? Number(raw) : '');
            }}
          ></nr-input>
        ` : kind === 'json' ? html`
          <nr-input
            .value=${value || ''}
            placeholder='{"key": "value"}'
            @nr-input=${(e: CustomEvent) => this.handleFieldChange(col.name, e.detail?.value ?? (e.target as any)?.value)}
          ></nr-input>
        ` : html`
          <nr-input
            .value=${value || ''}
            placeholder=${`Enter ${col.name}`}
            @nr-input=${(e: CustomEvent) => this.handleFieldChange(col.name, e.detail?.value ?? (e.target as any)?.value)}
          ></nr-input>
        `}

        ${hint ? html`<span class="field-description">${hint}</span>` : nothing}
      </div>
    `;
  }

  override render() {
    return html`
      <div class="config-panel-header">
        <span class="config-panel-title">Insert — ${this.tableName}</span>
        <button class="config-panel-close" @click=${this.handleClose}>&times;</button>
      </div>

      <div class="config-panel-content">
        ${this.editableColumns.length === 0 ? html`
          <div class="empty-state">No editable columns found for this table.</div>
        ` : html`
          <div class="config-section">
            <div class="config-section-header">
              <span class="config-section-title">Column Values</span>
              <span class="config-section-desc">Fill in values for the new row</span>
            </div>
            ${this.editableColumns.map(col => this.renderField(col))}
          </div>
        `}

        ${this.result ? html`
          <div class="config-section">
            ${this.result.success ? html`
              <div class="result-success">
                Row inserted${this.result.affectedRows ? ` (${this.result.affectedRows} row)` : ''}
              </div>
            ` : html`
              <div class="result-error">${this.result.error || 'Insert failed'}</div>
            `}
          </div>
        ` : nothing}
      </div>

      <div class="panel-footer">
        <button class="btn btn-cancel" @click=${this.handleClose} ?disabled=${this.inserting}>
          Cancel
        </button>
        <button
          class="btn btn-insert"
          @click=${this.handleInsert}
          ?disabled=${this.inserting || this.editableColumns.length === 0}
        >
          ${this.inserting ? 'Inserting...' : 'Insert'}
        </button>
      </div>
    `;
  }
